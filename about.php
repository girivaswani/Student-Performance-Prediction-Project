<h2 class="text-centre h2-aashish">Description of the Project</h2>
<p>
    <ul>
        <li class="li-aashish">The ability to predict the <b>performance tendency</b> of students is very important to improve their Learning skills. </li>
        <li class="li-aashish">It has become a <b>valuable knowledge</b> that can be used for different purposes.</li>
        <li class="li-aashish">For example, a strategic plan can be applied for the <b>development of a quality education.</b></li>
        <li class="li-aashish">This model proposes the application of Machine Learning algorithms to <b>predict the final grades</b> of the semester of college students based on their historical data, eventually identifying students who are weak and strong from academic points.</li>
        <li class="li-aashish">In the experimental studies,  well-known <b>data mining techniques </b> (decision tree classifier, random forest classifier, and K Nearest Classifier) are used.</li>
        <li class="li-aashish">The results will show the <b>effectiveness</b> of data mining learning (Machine Learning) techniques when predicting the performances of students.</li>
    </ul> 
</p>


